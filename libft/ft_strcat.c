char	*ft_strcat(char *dest, const char *src)
{
	int di;
	int si;

	di = 0;
	si = 0;
	while (dest[di])
		di++;
	while (src[si])
	{
		dest[di] = src[si];
		di++;
		si++;
	}
	dest[di] = '\0';
	return (dest);
}
