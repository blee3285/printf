#include "libft.h"

int	ft_isprime(int num)
{
	int	i;

	i = 3;
	if (num == 2)
		return (1);
	if ((num % 2 == 0) || (num < 2))
		return (0);
	while (i <= (num / 3))
	{
		if (num % i == 0)
			return (0);
		i += 2;
	}
	return (1);
}
